public class conversion {
    public static void main(String[] args) {
    //convert a to int 
    float a =10.232f;
    int con_a= (int)a;
    System.out.println(con_a);
    
   //convert b to float
   int b=10;
   float con_b= b;
  System.out.println(con_b);

  //convert c to int
  char c='c';
  int con_c= c;
  System.out.println(con_c);
    
  }    
    
}
