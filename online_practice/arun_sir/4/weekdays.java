class Days
{
    public static void main(String[] args) 
    { 
       int i,j;
       for(i=1;i<=2;i++)//week
       {
           System.out.println("WEEK: "+i);
           for(j=1;j<=7;j++)//day
           {
               System.out.println("DAY: "+j);
           }    
           System.out.println("------------");
       }
       
    }        
}
