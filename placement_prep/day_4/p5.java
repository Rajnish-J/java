// printing numbers using do while loop

import java.util.*;

class p5{
    static Scanner sc = new Scanner(System.in);
    public static void main(String[] args) {
        System.out.print("Enter the number: ");
        int n = sc.nextInt(), count = 1;
        do{
            System.out.println("Rajnish");
            count++;
        }
        while(count <= n);
    }
}